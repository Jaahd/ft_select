#include <string.h>
#include "libft.h"

void		ft_swap(char **s1, char **s2)
{
	char		*tmp;

	if (*s1 != NULL && *s2 != NULL)
	{
		tmp = *s1;
		*s1 = *s2;
		*s2 = tmp;
	}
}
