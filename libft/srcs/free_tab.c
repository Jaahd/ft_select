#include "libft.h"

int			free_tbl(char ***tbl)
{
	int			i;
	int			len;

	i = 0;
	len = tbl_len(*tbl);
	while (i < len)
	{
		free((*tbl)[i]);
		i++;
	}
	free(*tbl);
	return (0);
}
